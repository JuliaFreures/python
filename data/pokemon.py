class Pokemon:
    def __init__(self, nome, tipo, ataque, defesa, ascii_art):
        """Inicializa os atributos do Pokémon."""
        self.nome = nome
        self.tipo = tipo
        self.vida = 100  # Vida incial fixa
        self.ataque = max(0, ataque)  # Garante que o ataque seja positivo
        self.defesa = max(0, defesa)  # Garante que a defesa seja positiva
        self.ascii_art = ascii_art

    def exibir(self):
        """Exibe as informações do Pokémon."""
        print(f"Nome: {self.nome}")
        print(f"Tipo: {self.tipo}")
        print(f"Vida: {self.vida}")
        print(f"Ataque: {self.ataque}")
        print(f"Defesa: {self.defesa}")
        print(self.ascii_art)
        print("-" * 40)


